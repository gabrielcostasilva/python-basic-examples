yourName = input("Enter your name: ").strip()

print("The whole name is \'{}\'".format(yourName))
print("The first character is \'{}\'".format(yourName[0]))
print("The last character is \'{}\'".format(yourName[-1]))
print("From the first to the third: {}".format(yourName[:3]))
print("The fourth character is \'{}\'".format(yourName[3]))
print("All characters, but the first are \'{}\'".format(yourName[1:]))
print("The last two characters are \'{}\'".format(yourName[-2:]))